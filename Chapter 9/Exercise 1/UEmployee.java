public class UEmployee {
    String EmpName;
    int Salary;
    public void SetEmpName(String name) {
        EmpName = name;
    }
    public void setSalary(int salary) {
        Salary = salary;
    }
    public String GetEmpName(){
        return EmpName;
    }
    public int getSalary() {
        return Salary;
    }
}
